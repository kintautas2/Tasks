
function zero(argument)
{
	 if (argument !== undefined) {
          var op = argument;
          if (op.charAt(0) === "+")
          {
          	return 0 + op.charAt(1)*1;
          }
          if (op.charAt(0) === "-")
          {
          	return 0 - op.charAt(1)*1;
          }
          if (op.charAt(0) === "*")
          {
          	return 0 * op.charAt(1)*1;
          }
          if (op.charAt(0) === "/")
          {
          	if (op.charAt(1)*1 !== 0)
          	{
          		return 0 / op.charAt(1)*1;
          	} else return undefined;
          	
          }
    }
    else return "0";
}
